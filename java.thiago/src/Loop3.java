public class Loop3 {
    
    public static void main(String[] args) {
        // primeiro passo do for, declarar uma variavel, atÃ© onde vamos 
        // dar um parametro, para x maior igual a zero, subtrair 1, quando chegar em 0 ele para, por que a expressao "--" significa que vamos subtrair 1.
        for (int x=100; x>=0; x--){
            System.out.println("valor de x "+x);
        }
    }
}
